package com.company.Patterns;

public class Rectangle_pattern {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) {
            for (int j = 0; j < 5; j++) {
//                same line
                System.out.print("*");
            }
//            new line
            System.out.println("*");

        }
    }
}
